CREATE TABLE produto (
    id INT PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(100),
    preco DECIMAL(10,2)
);

CREATE TABLE ordem_pagamento (
    id INT PRIMARY KEY AUTO_INCREMENT,
    quantidade INT,
    valor DECIMAL(10, 2),
    produto_id BIGINT REFERENCES produto(id),
    added_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE guia_remessa (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    ordem_pagamento_id BIGINT REFERENCES ordem_pagamento(id),
    departamento VARCHAR(255),
    motivo VARCHAR(255),
    added_at DATE
);

CREATE TABLE midia_digital (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nome VARCHAR(255),
    descricao VARCHAR(255),
    added_at DATE,
    midia_digital_id BIGINT REFERENCES midia_digital(id)
);

